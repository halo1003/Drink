package com.example.toands.drink.Model;

public class Timeline {
    public String day;
    public String month;
    public String time;
    public String fullDay;

    public Timeline() {
    }

    public Timeline(String day, String month, String time, String fullDay) {
        this.day = day;
        this.month = month;
        this.time = time;
        this.fullDay = fullDay;
    }
}
